def kg_to_lbs(weight):
    return weight / 0.45

def lbs_to_kg(weight):
    return weight * 0.45

def cm_to_m(length_cm):
    return length_cm / 100

def m_to_cm(length_m):
    return length_m *100

def mile_to_kilometer(length_mile):
    return length_mile * 1.6

def kilometer_to_mile(length_kilometer):
    return length_kilometer / 1.6

